const setLanguage = (lang) => {
  const dict = content[lang];
  if (!dict) return;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-key]").forEach((el) => {
    const key = el.dataset.key;
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });
  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
  const cvLink = document.getElementById("cv-download-link");
  if (cvLink) {
    const cvPath = lang === "tr" ? cvLink.dataset.cvTr : cvLink.dataset.cvEn;
    if (cvPath) {
      cvLink.href = cvPath;
      cvLink.setAttribute("download", lang === "tr" ? "MERT_BURSALIOGLU_NOREFCV.pdf" : "cv_eng_no_ref.pdf");
    }
  }
  localStorage.setItem("preferredLang", lang);
};

const initLang = () => {
  const preferred = localStorage.getItem("preferredLang");
  const lang = preferred === "en" || preferred === "tr" ? preferred : "tr";
  setLanguage(lang);
};

const initLangButtons = () => {
  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });
};

const initScroll = () => {
  document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
    anchor.addEventListener("click", (e) => {
      const target = document.querySelector(anchor.getAttribute("href"));
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: "smooth", block: "start" });
      }
    });
  });
};

document.addEventListener("DOMContentLoaded", () => {
  initLang();
  initLangButtons();
  initScroll();
});
